# ==============================================================================
# SIZE REPORT
# ==============================================================================
TOTAL_SIZE=$(du -sh "$RELEASE_DIR" | cut -f1)
PIPELINE_SIZE=$(du -sh "$RELEASE_DIR/pipeline" | cut -f1)

echo ""
echo -e "${GREEN}══════════════════════════════════════════════════${NC}"
echo -e "${GREEN}  ✅ STEWARD DISTRIBUTION PACKAGED${NC}"
echo -e "══════════════════════════════════════════════════"
echo -e "  ${GRAY}Location     : $RELEASE_DIR${NC}"
echo -e "  ${GRAY}pipeline/    : $PIPELINE_SIZE${NC}"
echo -e "  ${GRAY}Total        : $TOTAL_SIZE${NC}"
echo -e "  ${GRAY}Core scripts : $CORE_COUNT${NC}"
echo -e "  ${GRAY}Translations : $TRANS_COUNT frozen${NC}"
echo -e "  ${GRAY}Canon hash   : ${CANON_HASH:0:24}...${NC}"
echo -e "  ${GRAY}Credentials  : EXCLUDED ✅${NC}"
echo -e "${GREEN}══════════════════════════════════════════════════${NC}\n"
